        // A large prime value smaller than the count of _randomizedLastNames
        const int _largePrime = 4999;

        public string GetObfuscatedGuid(string originalGuid)
        {
            if (originalGuid == null)
            {
                return null;
            }

            // This is a simple replacement cipher. It can easily be reversed by anyone who knows the replacment rules, but it's obfuscated by sight.
            return string.Join("", originalGuid.Select(c =>
            {
                switch (char.ToUpper(c))
                {
                    case '-': return '-';
                    case '0': return '9';
                    case '1': return '0';
                    case '2': return '5';
                    case '3': return '2';
                    case '4': return '4';
                    case '5': return '1';
                    case '6': return '6';
                    case '7': return '8';
                    case '8': return '3';
                    case '9': return '7';
                    case 'A': return 'E';
                    case 'B': return 'D';
                    case 'C': return 'C';
                    case 'D': return 'A';
                    case 'E': return 'B';
                    case 'F': return 'F';
                    default: return 'X';
                }
            }));
        }

        public string GetAnonymizedName(string originalName)
        {
            if (string.IsNullOrWhiteSpace(originalName))
            {
                return originalName;
            }

            var nameComponents = originalName.Trim().Split(' ').ToList();

            var originalLastName = nameComponents.Last();
            var newName = GetRandomLastName(originalLastName);

            // Something that looks like a username
            // Preserve any numbers at the end
            newName += string.Join("", originalLastName.Where(c => char.IsDigit(c)));

            // Preserve any initial capital letter
            if (char.IsUpper(originalLastName[0]))
            {
                if (originalLastName.Length == 4 && originalLastName[1] == '.' && originalLastName[3] == '.')
                {
                    newName = $"{originalLastName.Substring(0, 1)}.{newName.Substring(0, 1)}.";
                }
                else if (originalLastName.All(c => char.IsUpper(c)))
                {
                    // All caps
                    newName = string.Join("", newName.Select(c => char.ToUpper(c)));
                }
                else if (!char.IsUpper(newName[0]))
                {
                    newName = char.ToUpper(newName[0]) + newName.Substring(1);
                }
            }
            else
            {
                // All lower
                if (originalLastName.All(c => char.IsLower(c)))
                {
                    newName = string.Join("", newName.Select(c => char.ToLower(c)));
                }
                else
                {
                    newName = char.ToLower(newName[0]) + newName.Substring(1);
                }
            }

            // A normal name with first middle last components
            // Randomize the last name only
            if (nameComponents.Count > 1)
            {
                // If the last name was an initial, keep it as an initial
                if (originalLastName.Length == 2 && originalLastName.Last() == '.')
                {
                    newName = $"{newName[0]}.";
                }

                return $"{string.Join(" ", nameComponents.Take(nameComponents.Count - 1))} {newName}";
            }

            return newName;
        }

